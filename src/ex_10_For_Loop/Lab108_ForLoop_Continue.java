package ex_10_For_Loop;

public class Lab108_ForLoop_Continue {
    public static void main(String[] args) {
        for (int i=0; i<50; i++){      // 0 to 49, 50 times
            if (i == 5){         // only 5 will not print
                continue;   // skip below code, and move to top
            }

        }
    }
}
