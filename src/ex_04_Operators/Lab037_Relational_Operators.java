package ex_04_Operators;

public class Lab037_Relational_Operators {
    public static void main(String[] args) {

        // < Less Than
        // < = -> Less than or Equal to
        // > Greater than
        // > = -> Greater than or Equal to
        // == -> Equal to (but checking)
        // != -> Not equal

        // All of them will resul boolean output


        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

        int age_romi = 30;
        int age_raju = 30;
        boolean result = age_romi >= age_raju;
        System.out.println(result);

    }
}
