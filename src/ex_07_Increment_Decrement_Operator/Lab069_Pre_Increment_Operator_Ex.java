package ex_07_Increment_Decrement_Operator;

public class Lab069_Pre_Increment_Operator_Ex {
    public static void main(String[] args) {
        int a = 10;
        int result = ++a;
        System.out.println(a);
        System.out.println(result);


        // Line | a | Exp(result)
        // 5 | 10 | NA
        // 6 | 11 | 10
        // 7 | 11 |
        // 8 | 11


    }
}
