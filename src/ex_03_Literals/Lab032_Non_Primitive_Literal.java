package ex_03_Literals;

public class Lab032_Non_Primitive_Literal {
    public static void main(String[] args) {

        //Primitive Data Type (it is defined by Java Guys)
        //Primitive have Max,Min and size define.
        byte b = 10;  // 1 byte = 8 Bits
        int age = 35; // 4 Byte = 32 Bits

        //Non-Primitive Data Type (It is defined by users and also called a Reference data Type)
        //In Non-primitive have no Max,Min and size define.
        //Byte size - 8, Bits size - 64

        String name = "Java";  //String is bunch od char.
        int[] arrays_of_items = new int[10];

        // String, Arrays, Classes, enum....-->These are Non-Primitive data Type

    }
}
